package Week1.Day2;

public class AssignmentCalculator {
	public int add (int a, int b, int c)
	{
		int sum= a + b+ c;
		return sum;
	}
	public int sub (int a, int b)
	{
		int sub =a - b;
		return sub;
	}
	public double mult (double a , double b)
	{
		double mult = a * b;
		return mult;
	}
	public float divide (float a , float b)
	{
		float divide = a/b;
		return divide;
	}

}
